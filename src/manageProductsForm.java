    private void btn_EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EditActionPerformed

        String id = lbl_id.getText();
        String p_name = txt_Product_Name.getText();
        String tp = String.valueOf(jComboBox_type.getSelectedItem());
        int t_id = type.findTypeID(tp);
        if (id.equals("")) //no supplier to edit
            return;
        if(pr.checkProduct(p_name, t_id, false) == false) //validation checks
            return;
        Integer pr_id = Integer.valueOf(id);
        pr.updateProduct(p_name,t_id,pr_id);
        pr.fillProductTableAgain("");
        cleanForm();
    }//GEN-LAST:event_btn_EditActionPerformed

    private void btn_RemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RemoveActionPerformed

        if (lbl_id.getText().equals("")) //non supplier was selected
            return;
        int id = Integer.valueOf(lbl_id.getText());
        product p = new product();
        p.deleteProduct(id);
        p.fillProductTableAgain("");
        MainForm.lbl_Products_c.setText("Products count: "+ Integer.toString(MyFunction.countData("product")));
        cleanForm();
    }//GEN-LAST:event_btn_RemoveActionPerformed

