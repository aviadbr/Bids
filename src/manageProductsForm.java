    private void cleanForm()
    {
        lbl_id.setText("");
        txt_Product_Name.setText("");
        txt_Search.setText("");
        jComboBox_type.setSelectedIndex(0);
        tbl_product.requestFocus();
        tbl_product.changeSelection(0, 0, false, false);
        loadNewRow();
    }
    
    /*
        loads a new row from the table
    */
    private void loadNewRow()
    {
        int rowIndex;
        DefaultTableModel model = (DefaultTableModel)tbl_product.getModel();
        rowIndex = tbl_product.getSelectedRow();
        lbl_id.setText(model.getValueAt(rowIndex,0).toString());
        txt_Product_Name.setText(model.getValueAt(rowIndex,1).toString());
        String tp = model.getValueAt(rowIndex,2).toString();
        if(type.findTypeID(tp)<0)
            jComboBox_type.setSelectedIndex(0);
        else
            jComboBox_type.setSelectedItem(tp);
    }
    
